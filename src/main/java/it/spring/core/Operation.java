package it.spring.core;

public class Operation {

	public int sommaValori(int numero_1, int numero_2) {
		int sommaValori = numero_1 + numero_2;
		return sommaValori;
	}
	
	public int sottraiValori(int numero_1, int numero_2) {
		int sottrazioneValori = numero_1 - numero_2;
		return sottrazioneValori;
	}
	
	public int moltiplicaValori(int numero_1, int numero_2) {
		int moltiplicazioneValori = numero_1 * numero_2;
		return moltiplicazioneValori;
	}
	
	public int dividiValori(int numero_1, int numero_2) {
		int divisioneValori = numero_1 / numero_2;
		return divisioneValori;
	}
}
